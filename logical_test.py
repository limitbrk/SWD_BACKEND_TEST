def main():
    print("Convert Number to Thai Text.")
    num = input("Enter your number: ")
    print("Your result is: ",thai(num))

def thai(num: int) -> str:
    chdiff = ord('๑')-ord('1')
    result = [chr(ord(i)+chdiff) for i in str(num)]
    return "".join(result)

main()